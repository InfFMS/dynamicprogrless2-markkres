"""
Задача: Сколькими способами можно получить 1341 рубль, имея в распоряжении 
монетки номиналом 1, 2, 5 рублей?

Монеты можно использовать неограниченное количество раз.
Порядок монет не важен (то есть наборы монет считаются одинаковыми, если 
содержат одинаковое количество монет каждого номинала).

Ответ должен быть напечатан как одно число.

"""
total=1341
coins=[1,2,5]
dp=[0]*(total+1)
dp[0]=1
for c in coins:
    for i in range(c,total+1):
        dp[i]+=dp[i-c]
print(dp[total])
